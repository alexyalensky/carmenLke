const path = join(dirname(fileURLToPath(import.meta.url)), '..', 'src/data/almanacExtras.ts')

if (process.argv[1]?.endsWith('apply-hebrew-names.mjs')) {
  let src = readFileSync(path, 'utf8')
  let count = 0

  for (const [en, heName] of Object.entries(ALMANAC_NAME_HE)) {
    // How the value appears as a single-quoted JS string literal in the source.
    const enToken = en.replace(/\\/g, '\\\\').replace(/'/g, "\\'")
    const escHe = heName.replace(/\\/g, '\\\\').replace(/'/g, "\\'")
    // Regex-escape the source token so backslashes/apostrophes match literally.
    const enPattern = enToken.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')
    // Allow escaped characters inside the existing name literal (e.g. \').
    const re = new RegExp(`\\{ name: '(?:\\\\.|[^'\\\\])*', nameEn: '${enPattern}'`, 'g')
    const replacement = `{ name: '${escHe}', nameEn: '${enToken}'`
    const matches = src.match(re)
    if (matches) {
      count += matches.length
      src = src.replace(re, replacement)
    }
  }

  writeFileSync(path, src, 'utf8')
  console.log(`Updated ${count} almanac name fields`)
}
