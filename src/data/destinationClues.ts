export function getVagueClueForCity(cityId: string, usedTexts?: Set<string>, slotIndex = 0): string {
  const pool = destinationClues[cityId]
  if (!pool?.length) return 'החשוד השאיל שאלות על יעד מסתורי.'
  if (usedTexts) return pickUnusedFromPool(pool, usedTexts, slotIndex)
  return pool[Math.floor(Math.random() * pool.length)]!
}

function pickUnusedFromPool(pool: string[], usedTexts: Set<string>, slotIndex: number): string {
  const available = pool.filter((t) => !usedTexts.has(t))
  const pickFrom = available.length > 0 ? available : pool
  return pickFrom[slotIndex % pickFrom.length]!
}

export function getVagueClueForWrongCity(
  excludeCityId: string,
  usedTexts?: Set<string>,
  slotIndex = 0,
): string {
  const allWrongClues = Object.entries(destinationClues)
    .filter(([id]) => id !== excludeCityId)
    .flatMap(([, clues]) => clues)
  if (!allWrongClues.length) return 'העד נתן רמז מטעה.'
  if (usedTexts) return pickUnusedFromPool(allWrongClues, usedTexts, slotIndex)
  return allWrongClues[Math.floor(Math.random() * allWrongClues.length)]!
}
